Valor1 = float(input("Digite o primeiro valor: "))
Valor2 = float(input("Digite o segundo valor: "))

soma = Valor1 + Valor2
sub = Valor1 - Valor2
mult = Valor1 * Valor2
div = Valor1 / Valor2

print("O valor da soma é: {}" .format(soma))
